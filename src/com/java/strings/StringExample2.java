package com.java.strings;

public class StringExample2 {

	public static void main(String[] args) {
		int a =10; // primitive data type
		a =a+20;
		
		System.out.println(a); // store the new value in same memory location
		
		String s = "Welcome"; // non primitive data type
		s = s+" to java world";


		System.out.println(s); // store the new value in new memory location
		 
		
	}

}
