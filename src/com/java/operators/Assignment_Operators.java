package com.java.operators;

public class Assignment_Operators {

	public static void main(String[] args) {
		
	//Assignment operators : +=, -=, *=, /=, %= 
		int i=4;
		i+=2;
		System.out.println("i value after addition is " +i);
		
		i-=1;
		System.out.println("i value after substraction is " +i);
		
		i*=2;
		System.out.println("i value after multplication is " +i);
		
		i/=5;
		System.out.println("i value after division is " +i);
		
		i%=4;
		System.out.println("i value after modular is " +i);
		
		

	}

}
