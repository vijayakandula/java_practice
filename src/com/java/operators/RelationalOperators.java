package com.java.operators;

public class RelationalOperators {

	public static void main(String[] args) {
		// Relational Operators
		// ==
		// !=
		// >
		// <
		// >=
		// <=
		
		int a = 25;
		int b = 26;
		
		System.out.println(a == b);
		System.out.println(a != b);
		System.out.println(a > b);
		System.out.println(a > b);
		System.out.println(a >= b);
		System.out.println(a <= b);
	}


}
